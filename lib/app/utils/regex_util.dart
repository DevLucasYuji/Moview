class RegexUtils {
  static bool validEmail(String email) {
    var pattern =
        r"[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?";

    RegExp regex = RegExp(pattern);
    return regex.hasMatch(email);
  }
}
